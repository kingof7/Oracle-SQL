SELECT  SUB.SUB_NAME,
        SUB.SUB_PROF,
        STU_NAME
FROM 
(SELECT STU_NAME,STU_NO FROM STUDENT WHERE STU_NAME='김인중') S,
(SELECT STU_NO, SUB_NO FROM ENROL) E,
(SELECT SUB_NO, SUB_NAME, SUB_PROF FROM SUBJECT) SUB
WHERE S.STU_NO = E.STU_NO
AND E.SUB_NO = SUB.SUB_NO
AND STU_NAME = S.STU_NAME;

