SELECT  S.STU_NO,
        STU_NAME,
        STU_GRADE,
        ENR_GRADE,
        SUB_NAME,
        SUB_PROF,
        SCORE_NAME
FROM STUDENT S, ENROL E, GRADE G, SUBJECT SUB
WHERE S.STU_NO = E.STU_NO
AND E.SUB_NO = SUB.SUB_NO
AND ENR_GRADE BETWEEN SCORE_FROM AND SOCRE_TO;
