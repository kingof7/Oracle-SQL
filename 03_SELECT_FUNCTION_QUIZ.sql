SELECT LOWER(FIRST_NAME) 사원이름
FROM EMPLOYEES;

SELECT LOWER(FIRST_NAME) 사원이름,
LOWER(JOB_ID) 사원직군
FROM EMPLOYEES;

SELECT UPPER(FIRST_NAME) 사원이름
FROM EMPLOYEES;

SELECT UPPER(FIRST_NAME) 사원이름,
UPPER(JOB_ID) 사원직군
FROM EMPLOYEES;

SELECT INITCAP(FIRST_NAME) 사원이름
FROM EMPLOYEES;

SELECT CONCAT(FIRST_NAME,JOB_ID) 사원연결
FROM EMPLOYEES;

SELECT FIRST_NAME, SUBSTR(FIRST_NAME,1,2) 문자열추출
FROM EMPLOYEES;

SELECT FIRST_NAME, JOB_ID, SUBSTR(JOB_ID,4,2) 문자열추출
FROM EMPLOYEES;

SELECT FIRST_NAME, PHONE_NUMBER, SUBSTR(PHONE_NUMBER, 1, 3) 지역번호
FROM EMPLOYEES;

SELECT LAST_NAME
FROM EMPLOYEES
WHERE LAST_NAME LIKE '%n';

SELECT FIRST_NAME, LENGTH(FIRST_NAME) 문자길이
FROM EMPLOYEES;

SELECT FIRST_NAME, JOB_ID, LENGTH(FIRST_NAME) 문자길이
FROM EMPLOYEES;

SELECT FIRST_NAME, INSTR(FIRST_NAME, 'a') "a의 위치"
FROM EMPLOYEES;

SELECT JOB_ID, INSTR(JOB_ID, 'A') "A의 위치"
FROM EMPLOYEES;

SELECT RPAD(FIRST_NAME,15,'&') 패턴
FROM EMPLOYEES;

SELECT LPAD(JOB_ID,20,'%') 패턴
FROM EMPLOYEES;

