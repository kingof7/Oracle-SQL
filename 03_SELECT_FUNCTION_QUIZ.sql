SELECT EMPLOYEE_ID, SALARY, MOD(SALARY, 1000) 나머지
FROM EMPLOYEES;

SELECT EMPLOYEE_ID, FIRST_NAME, HIRE_DATE, HIRE_DATE+100 "100 후"
FROM EMPLOYEES;

SELECT EMPLOYEE_ID, FIRST_NAME, HIRE_DATE, TRUNC(SYSDATE - HIRE_DATE) "근무일자"
FROM EMPLOYEES;

SELECT HIRE_DATE, ADD_MONTHS(HIRE_DATE,3) 삼개월후날짜
FROM EMPLOYEES;

SELECT HIRE_DATE, NEXT_DAY(HIRE_DATE, '토') 토요일날짜
FROM EMPLOYEES;

SELECT HIRE_DATE, LAST_DAY(HIRE_DATE) 마지막날짜
FROM EMPLOYEES;

SELECT HIRE_DATE, ROUND(HIRE_DATE, 'YY') 반올림
FROM EMPLOYEES;

SELECT HIRE_DATE, TRUNC(HIRE_DATE, 'YY') 버림
FROM EMPLOYEES;

SELECT EMPLOYEE_ID, FIRST_NAME, NVL(TO_CHAR(MANAGER_ID),'CEO') 관리자번호 -- NVL안에 두 데이터는 서로 자료형이 같아야됨
FROM EMPLOYEES;

SELECT EMPLOYEE_ID, FIRST_NAME, SALARY, COMMISSION_PCT, (SALARY+NVL(COMMISSION_PCT,0))*12 연봉계산
FROM EMPLOYEES;
